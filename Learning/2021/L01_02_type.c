#include <stdio.h>

int main(){
  int integer = 0; /* 整数値 */
  double number = 0.22; /* 実数値 */
  char character = 'a'; /* 文字 (1文字) */

  /* 整数値として表示 */
  printf("%d\n", integer);

  /* 実数値として表示 */
  printf("%f\n", number);

  /* 文字として表示 */
  printf("%c\n", character);

  return 0;
}

